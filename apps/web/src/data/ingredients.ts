// 成分検索用のヘルパー関数
export function getIngredientBySlug(slug: string): Ingredient | undefined {
  return ingredientsData.find((ingredient) => ingredient.slug === slug);
}

export function getIngredientsByCategory(category: string): Ingredient[] {
  return ingredientsData.filter(
    (ingredient) => ingredient.category === category,
  );
}

export function getAllIngredientSlugs(): string[] {
  return ingredientsData.map((ingredient) => ingredient.slug);
}
