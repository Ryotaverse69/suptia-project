async function seedMinerals() {
  console.log('ミネラル成分データの投入を開始します...\n');

  for (const ingredient of ingredients) {
    try {
      console.log(`Creating: ${ingredient.name}...`);
      const result = await client.create(ingredient);
      console.log(`✓ Created: ${ingredient.name} (${result._id})\n`);
    } catch (error) {
      console.error(`✗ Error creating ${ingredient.name}:`, error.message);
    }
  }

  console.log('\n完了しました！');
  console.log('Sanity Studio (http://localhost:3333) で確認してください。');
}

seedMinerals();
